#include "3-calc.h"
/**
 * op_add - adds
 *@a: 1st
 *@b: 2nd
 * Return: result
 */
int op_add(int a, int b)
{
return (a + b);
}
/**
 * op_sub - subtracts
 *@a: 1st
 *@b: 2nd
 * Return: result
 */
int op_sub(int a, int b)
{
return (a - b);
}
/**
 * op_mul - multiplies
 *@a: 1st
 *@b: 2nd
 * Return: result
 */
int op_mul(int a, int b)
{
return (a * b);
}
/**
 * op_div - divides
 *@a: 1st
 *@b: 2nd
 * Return: result
 */
int op_div(int a, int b)
{
return (a / b);
}
/**
 * op_mod - modulus
 *@a: 1st
 *@b: 2nd
 * Return: result
 */
int op_mod(int a, int b)
{
return (a % b);
}
